class OrderData {
  String _title;
  String _person;
  String _mobile;
  String _address;
  String _zone;
  OrderData(this._title, this._person, this._mobile, this._address, this._zone);

  String getTitle() => _title;
  String getPerson() => _person;
  String getMobile() => _mobile;
  String getAddress() => _address;
  String getZone() => _zone;

}
